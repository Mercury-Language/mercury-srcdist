%-----------------------------------------------------------------------------%

:- module try_io_else.
:- interface.

:- import_module io.

:- pred main(io::di, io::uo) is det.

%-----------------------------------------------------------------------------%
%-----------------------------------------------------------------------------%

:- implementation.

:- import_module exception.

%-----------------------------------------------------------------------------%

main(!IO) :-
    (try [io(!IO)]
        true
    then
        true
    else    % not allowed
        true
    ).

