:- module binary_stdout.
:- interface.

:- import_module io.

:- pred main(io::di, io::uo) is det.

%---------------------------------------------------------------------------%
%---------------------------------------------------------------------------%

:- implementation.

:- import_module int.

%---------------------------------------------------------------------------%

main(!IO) :-
    int.fold_up(io.write_byte, 0, 255, !IO),
    io.stdout_binary_stream(Stream, !IO),
    int.fold_up(io.write_byte(Stream), 0, 255, !IO).
